#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*sb;
	size_t	i;

	sb = s;
	i = 0;
	while (i < n)
	{
		sb[i] = c;
		i++;
	}
	return (sb);
}
